/* A function that will sum all of the elements in the given array
 * a: pointer to the input array
 * n: number of elements in the array
 * Return value: sum of the first n elements in a */

int sum(const int* a, int n) {
  int sum_result = 0;
  for (int i=0;i<n;i++){
    sum_result+=a[i];
  } 

  return 0;

  
}
